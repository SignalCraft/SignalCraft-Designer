#ifndef OVERLOADRESULT_H
#define OVERLOADRESULT_H

/**
 * The OverloadResult enum represents the result of a block's overloading
 * logic, or NONE if the block does not use overloading
 */
enum OverloadResult { OVERLOADRESULT_NONE, OVERLOADRESULT_INT, OVERLOADRESULT_AFP, OVERLOADRESULT_FLOAT };

#endif // OVERLOADRESULT_H
